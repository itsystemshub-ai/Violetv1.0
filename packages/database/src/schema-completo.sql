## 🔧 Triggers y Stored Procedures

### Trigger: VENTAS_AI0 (After Insert)

```sql
-- Este trigger se ejecuta después de insertar una venta
-- Actualiza el CXC si es venta a crédito
-- Actualiza el inventario
-- Actualiza el contabilidad

CREATE TRIGGER VENTAS_AI0
AFTER INSERT ON VENTAS
FOR EACH ROW
BEGIN
    -- Si es venta a crédito, crear registro en CXC
    IF NEW.CREDITO > 0 THEN
        INSERT INTO CXC (
            CLIENTE_CODIGO,
            DOCUMENTO,
            TIPO_DOCUMENTO,
            FECHA,
            FECHA_VENCIMIENTO,
            TOTAL_OPERACION,
            TOTAL_BASE_IMPONIBLE,
            TOTAL_IMPUESTO,
            SALDO,
            ESTATUS,
            VENTA_CORRELATIVO,
            CREATED_AT
        ) VALUES (
            NEW.CLIENTE_CODIGO,
            NEW.DOCUMENTO,
            'FCR',
            NEW.FECHA_EMISION,
            NEW.FECHA_VENCIMIENTO,
            NEW.TOTAL_OPERACION,
            NEW.TOTAL_BASE_IMPONIBLE,
            NEW.TOTAL_IMPUESTO,
            NEW.CREDITO,
            'P',
            NEW.CORRELATIVO,
            CURRENT_TIMESTAMP
        );
    END IF;
    
    -- Actualizar inventario (si impacta inventario)
    IF NEW.IMPACTA_INVENTARIO = 'S' THEN
        -- El detalle se maneja en otro trigger
        UPDATE CONFIGuracion
        SET ULTIMA_ACTUALIZACION = CURRENT_TIMESTAMP
        WHERE PARAMETRO = 'INVENTARIO';
    END IF;
END;
```

### Stored Procedure: CALCULAR_RETENCION_IVA

```sql
CREATE PROCEDURE CALCULAR_RETENCION_IVA(
    MONTO_IVA DECIMAL(18,2),
    CONTRIBUYENTE_ESPECIAL CHAR(1)
)
RETURNS (
    PORCENTAJE DECIMAL(5,2),
    MONTO_RETENIDO DECIMAL(18,2)
)
AS
BEGIN
    -- Si es contribuyente especial, retiene 100%
    -- Si es ordinario, retiene 75%
    IF CONTRIBUYENTE_ESPECIAL = 'T' THEN
        PORCENTAJE = 100;
    ELSE
        PORCENTAJE = 75;
    END IF;
    
    MONTO_RETENIDO = (MONTO_IVA * PORCENTAJE) / 100;
    
    SUSPEND;
END
```

### Stored Procedure: CALCULAR_IGTF

```sql
CREATE PROCEDURE CALCULAR_IGTF(
    TOTAL_OPERACION DECIMAL(18,2),
    ES_PAGO_DIVISAS CHAR(1)
)
RETURNS (
    TASA DECIMAL(5,2),
    MONTO_IGTF DECIMAL(18,2),
    TOTAL_CON_IGTF DECIMAL(18,2)
)
AS
BEGIN
    IF ES_PAGO_DIVISAS = 'T' THEN
        TASA = 3;
        MONTO_IGTF = (TOTAL_OPERACION * TASA) / 100;
        TOTAL_CON_IGTF = TOTAL_OPERACION + MONTO_IGTF;
    ELSE
        TASA = 0;
        MONTO_IGTF = 0;
        TOTAL_CON_IGTF = TOTAL_OPERACION;
    END IF;
    
    SUSPEND;
END
```

