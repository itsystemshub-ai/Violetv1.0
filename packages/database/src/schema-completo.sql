## 📌 Notas de Migración

### Consideraciones Firebird → SQLite/PostgreSQL

1. **Tipos de Datos:**
   - `DECIMAL(18,2)` → `DECIMAL(18,2)` (igual)
   - `DOUBLE PRECISION` → `REAL` o `DOUBLE PRECISION`
   - `DATE` → `DATE` (igual)
   - `TIME` → `TIME` (igual)
   - `TIMESTAMP` → `DATETIME` (SQLite) / `TIMESTAMP` (PostgreSQL)

2. **Autoincrement:**
   - Firebird: `GENERATOR` + `TRIGGER`
   - SQLite: `AUTOINCREMENT`
   - PostgreSQL: `SERIAL` o `IDENTITY`

3. **Triggers:**
   - La sintaxis varía ligeramente entre motores
   - Revisar cada trigger cuidadosamente

4. **Stored Procedures:**
   - Firebird: `RETURNS` + `SUSPEND`
   - PostgreSQL: `RETURNS TABLE` + `RETURN QUERY`
   - SQLite: No soporta SP, usar funciones o lógica en aplicación

