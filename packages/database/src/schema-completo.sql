## 🎯 Vistas Principales

### VISTA_LIBRO_VENTAS

```sql
CREATE VIEW VISTA_LIBRO_VENTAS AS
SELECT 
    V.FECHA_EMISION AS FECHA,
    V.DOCUMENTO AS NUMERO_FACTURA,
    V.NUMERO_CONTROL,
    C.RIF AS RIF_CLIENTE,
    C.NOMBRE AS NOMBRE_CLIENTE,
    V.TOTAL_BASE_IMPONIBLE AS BASE_IMPONIBLE,
    V.TOTAL_IMPUESTO_FISCAL AS IMPUESTO_IVA,
    V.TOTAL_EXENTO AS VENTA_EXENTA,
    V.TOTAL_RETENCION_IVA AS RETENCION_IVA,
    V.TOTAL_OPERACION AS TOTAL_VENTA
FROM VENTAS V
INNER JOIN CLIENTES C ON V.CLIENTE_CODIGO = C.CODIGO
WHERE V.ESTATUS = 'R'
  AND V.IMPACTA_CONTABILIDAD = 'S'
ORDER BY V.FECHA_EMISION, V.DOCUMENTO;
```

