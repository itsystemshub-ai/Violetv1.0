-- Procedimiento: ACTUALIZAR_SALDO_CLIENTE
-- Actualiza el saldo total de un cliente
CREATE OR ALTER PROCEDURE ACTUALIZAR_SALDO_CLIENTE (
    CLIENTE_CODIGO VARCHAR(15)
)
AS
DECLARE VARIABLE TOTAL_DEBITOS DECIMAL(18,2);
DECLARE VARIABLE TOTAL_CREDITOS DECIMAL(18,2);
DECLARE VARIABLE TOTAL_ANTICIPOS DECIMAL(18,2);
BEGIN
    -- Sumar todos los débitos (ventas, notas de débito)
    SELECT COALESCE(SUM(TOTAL_OPERACION), 0)
    FROM CXC
    WHERE CLIENTE_CODIGO = :CLIENTE_CODIGO
      AND ESTATUS <> 'C'
    INTO :TOTAL_DEBITOS;
    
    -- Sumar todos los créditos (pagos, notas de crédito)
    SELECT COALESCE(SUM(MONTO), 0)
    FROM PAGOS_CXC
    WHERE CXC_CORRELATIVO IN (
        SELECT CORRELATIVO FROM CXC WHERE CLIENTE_CODIGO = :CLIENTE_CODIGO
    )
    INTO :TOTAL_CREDITOS;
    
    -- Sumar anticipos
    SELECT COALESCE(SUM(ANTICIPO), 0)
    FROM VENTAS
    WHERE CLIENTE_CODIGO = :CLIENTE_CODIGO
      AND ESTATUS = 'R'
    INTO :TOTAL_ANTICIPOS;
    
    -- Actualizar el cliente
    UPDATE CLIENTES
    SET TOTAL_DEBITOS = :TOTAL_DEBITOS,
        TOTAL_CREDITOS = :TOTAL_CREDITOS + :TOTAL_ANTICIPOS,
        TOTAL_SALDO = :TOTAL_DEBITOS - :TOTAL_CREDITOS - :TOTAL_ANTICIPOS,
        UPDATED_AT = CURRENT_TIMESTAMP
    WHERE CODIGO = :CLIENTE_CODIGO;
END;

-- Procedimiento: ACTUALIZAR_SALDO_PROVEEDOR
-- Actualiza el saldo total de un proveedor
CREATE OR ALTER PROCEDURE ACTUALIZAR_SALDO_PROVEEDOR (
    PROVEEDOR_CODIGO VARCHAR(15)
)
AS
DECLARE VARIABLE TOTAL_DEBITOS DECIMAL(18,2);
DECLARE VARIABLE TOTAL_CREDITOS DECIMAL(18,2);
BEGIN
    -- Sumar todos los débitos (compras)
    SELECT COALESCE(SUM(TOTAL_OPERACION), 0)
    FROM CXP
    WHERE PROVEEDOR_CODIGO = :PROVEEDOR_CODIGO
      AND ESTATUS <> 'C'
    INTO :TOTAL_DEBITOS;
    
    -- Sumar todos los créditos (pagos)
    SELECT COALESCE(SUM(MONTO), 0)
    FROM PAGOS_CXP
    WHERE CXP_CORRELATIVO IN (
        SELECT CORRELATIVO FROM CXP WHERE PROVEEDOR_CODIGO = :PROVEEDOR_CODIGO
    )
    INTO :TOTAL_CREDITOS;
    
    -- Actualizar el proveedor
    UPDATE PROVEEDORES
    SET TOTAL_DEBITOS = :TOTAL_DEBITOS,
        TOTAL_CREDITOS = :TOTAL_CREDITOS,
        TOTAL_SALDO = :TOTAL_DEBITOS - :TOTAL_CREDITOS,
        UPDATED_AT = CURRENT_TIMESTAMP
    WHERE CODIGO = :PROVEEDOR_CODIGO;
END;

