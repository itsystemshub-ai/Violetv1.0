-- Procedimiento: GENERAR_NUMERO_DOCUMENTO
-- Genera un número de documento con formato
CREATE OR ALTER PROCEDURE GENERAR_NUMERO_DOCUMENTO (
    TIPO_DOCUMENTO VARCHAR(10),
    SUCURSAL_CODIGO VARCHAR(15)
)
RETURNS (
    NUMERO_DOCUMENTO VARCHAR(20)
)
AS
DECLARE VARIABLE ULTIMO_NUMERO INTEGER;
DECLARE VARIABLE PREFIJO VARCHAR(5);
DECLARE VARIABLE LONGITUD INTEGER;
DECLARE VARIABLE NUMERO_CEROS VARCHAR(20);
BEGIN
    -- Obtener información de la numeración
    SELECT ULTIMO_NUMERO, PREFIJO, LONGITUD
    FROM NUMERACION_DOCUMENTOS
    WHERE TIPO_DOCUMENTO = :TIPO_DOCUMENTO
      AND (SUCURSAL_CODIGO = :SUCURSAL_CODIGO OR SUCURSAL_CODIGO IS NULL)
    INTO :ULTIMO_NUMERO, :PREFIJO, :LONGITUD;
    
    IF (ULTIMO_NUMERO IS NULL) THEN
        ULTIMO_NUMERO = 0;
    
    IF (PREFIJO IS NULL) THEN
        PREFIJO = '';
    
    IF (LONGITUD IS NULL) THEN
        LONGITUD = 8;
    
    -- Incrementar el número
    ULTIMO_NUMERO = ULTIMO_NUMERO + 1;
    
    -- Actualizar la numeración
    UPDATE NUMERACION_DOCUMENTOS
    SET ULTIMO_NUMERO = :ULTIMO_NUMERO,
        UPDATED_AT = CURRENT_TIMESTAMP
    WHERE TIPO_DOCUMENTO = :TIPO_DOCUMENTO
      AND (SUCURSAL_CODIGO = :SUCURSAL_CODIGO OR SUCURSAL_CODIGO IS NULL);
    
    -- Generar el número con ceros a la izquierda
    NUMERO_CEROS = '00000000000000000000';
    NUMERO_DOCUMENTO = PREFIJO || SUBSTRING(NUMERO_CEROS FROM 1 FOR (LONGITUD - CHAR_LENGTH(ULTIMO_NUMERO))) || ULTIMO_NUMERO;
    
    SUSPEND;
END;

