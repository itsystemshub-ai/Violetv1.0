-- Procedimiento: ACTUALIZAR_EXISTENCIA
-- Actualiza la existencia de un producto después de un movimiento
CREATE OR ALTER PROCEDURE ACTUALIZAR_EXISTENCIA (
    PRODUCTO_CODIGO VARCHAR(15),
    DEPOSITO_CODIGO VARCHAR(15),
    CANTIDAD DECIMAL(18,4),
    TIPO_MOVIMIENTO CHAR(3), -- ENT=Entrada, SAL=Salida
    COSTO DECIMAL(18,4)
)
AS
DECLARE VARIABLE EXISTENCIA_ACTUAL DECIMAL(18,4);
DECLARE VARIABLE COSTO_PROMEDIO DECIMAL(18,4);
DECLARE VARIABLE NUEVO_COSTO_PROMEDIO DECIMAL(18,4);
DECLARE VARIABLE TOTAL_COSTO DECIMAL(18,2);
BEGIN
    -- Verificar si existe el registro de existencia
    SELECT COALESCE(CANTIDAD, 0), COALESCE(COSTO_PROMEDIO, 0)
    FROM EXISTENCIAS
    WHERE PRODUCTO_CODIGO = :PRODUCTO_CODIGO
      AND DEPOSITO_CODIGO = :DEPOSITO_CODIGO
    INTO :EXISTENCIA_ACTUAL, :COSTO_PROMEDIO;
    
    IF (EXISTENCIA_ACTUAL IS NULL) THEN
        EXISTENCIA_ACTUAL = 0;
    
    IF (COSTO_PROMEDIO IS NULL) THEN
        COSTO_PROMEDIO = 0;
    
    -- Procesar según el tipo de movimiento
    IF (TIPO_MOVIMIENTO = 'ENT') THEN
        -- Entrada: recalcular costo promedio
        TOTAL_COSTO = (EXISTENCIA_ACTUAL * COSTO_PROMEDIO) + (CANTIDAD * COSTO);
        NUEVO_COSTO_PROMEDIO = TOTAL_COSTO / (EXISTENCIA_ACTUAL + CANTIDAD);
        
        -- Actualizar o insertar existencia
        UPDATE OR INSERT INTO EXISTENCIAS (
            PRODUCTO_CODIGO,
            DEPOSITO_CODIGO,
            CANTIDAD,
            COSTO_PROMEDIO,
            ULTIMA_COMPRA
        ) VALUES (
            :PRODUCTO_CODIGO,
            :DEPOSITO_CODIGO,
            EXISTENCIA_ACTUAL + CANTIDAD,
            :NUEVO_COSTO_PROMEDIO,
            CURRENT_DATE
        ) MATCHING (PRODUCTO_CODIGO, DEPOSITO_CODIGO);
        
    ELSE IF (TIPO_MOVIMIENTO = 'SAL') THEN
        -- Salida: disminuir existencia al costo promedio actual
        UPDATE OR INSERT INTO EXISTENCIAS (
            PRODUCTO_CODIGO,
            DEPOSITO_CODIGO,
            CANTIDAD,
            COSTO_PROMEDIO,
            ULTIMA_VENTA
        ) VALUES (
            :PRODUCTO_CODIGO,
            :DEPOSITO_CODIGO,
            EXISTENCIA_ACTUAL - CANTIDAD,
            :COSTO_PROMEDIO,
            CURRENT_DATE
        ) MATCHING (PRODUCTO_CODIGO, DEPOSITO_CODIGO);
END;

-- Procedimiento: VERIFICAR_STOCK
-- Verifica si hay suficiente stock para una venta
CREATE OR ALTER PROCEDURE VERIFICAR_STOCK (
    PRODUCTO_CODIGO VARCHAR(15),
    DEPOSITO_CODIGO VARCHAR(15),
    CANTIDAD_SOLICITADA DECIMAL(18,4)
)
RETURNS (
    STOCK_DISPONIBLE DECIMAL(18,4),
    SUFICIENTE CHAR(1)
)
AS
BEGIN
    SELECT COALESCE(CANTIDAD, 0)
    FROM EXISTENCIAS
    WHERE PRODUCTO_CODIGO = :PRODUCTO_CODIGO
      AND DEPOSITO_CODIGO = :DEPOSITO_CODIGO
    INTO :STOCK_DISPONIBLE;
    
    IF (STOCK_DISPONIBLE IS NULL) THEN
        STOCK_DISPONIBLE = 0;
    
    IF (STOCK_DISPONIBLE >= CANTIDAD_SOLICITADA) THEN
        SUFICIENTE = 'S';
    ELSE
        SUFICIENTE = 'N';
    
    SUSPEND;
END;

