-- Procedimiento: CONCILIAR_MOVIMIENTO_BANCO
-- Marca un movimiento bancario como conciliado
CREATE OR ALTER PROCEDURE CONCILIAR_MOVIMIENTO_BANCO (
    MOVIMIENTO_CORRELATIVO INTEGER,
    PERIODO_CONCILIACION VARCHAR(7)
)
AS
BEGIN
    UPDATE MOVIMIENTOS_BANCOS
    SET CONCILIADO = 'T',
        FECHA_CONCILIACION = CURRENT_DATE,
        PERIODO_CONCILIACION = :PERIODO_CONCILIACION
    WHERE CORRELATIVO = :MOVIMIENTO_CORRELATIVO;
END;

-- Procedimiento: DESCONCILIAR_MOVIMIENTO_BANCO
-- Marca un movimiento bancario como no conciliado
CREATE OR ALTER PROCEDURE DESCONCILIAR_MOVIMIENTO_BANCO (
    MOVIMIENTO_CORRELATIVO INTEGER
)
AS
BEGIN
    UPDATE MOVIMIENTOS_BANCOS
    SET CONCILIADO = 'F',
        FECHA_CONCILIACION = NULL,
        PERIODO_CONCILIACION = NULL
    WHERE CORRELATIVO = :MOVIMIENTO_CORRELATIVO;
END;

