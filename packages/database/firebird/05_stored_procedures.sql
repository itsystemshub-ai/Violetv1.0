-- Procedimiento: OBTENER_SECUENCIA
-- Obtiene el siguiente número de una secuencia
CREATE OR ALTER PROCEDURE OBTENER_SECUENCIA (
    NOMBRE_SECUENCIA VARCHAR(50)
)
RETURNS (
    PROXIMO_NUMERO INTEGER
)
AS
DECLARE VARIABLE ULTIMO INTEGER;
DECLARE VARIABLE INCREMENTO INTEGER;
BEGIN
    SELECT ULTIMO_VALOR, INCREMENTO
    FROM SECUENCIAS
    WHERE NOMBRE = :NOMBRE_SECUENCIA
    INTO :ULTIMO, :INCREMENTO;
    
    IF (ULTIMO IS NULL) THEN
        ULTIMO = 0;
    
    IF (INCREMENTO IS NULL) THEN
        INCREMENTO = 1;
    
    PROXIMO_NUMERO = ULTIMO + INCREMENTO;
    
    -- Actualizar la secuencia
    UPDATE SECUENCIAS
    SET ULTIMO_VALOR = :PROXIMO_NUMERO,
        UPDATED_AT = CURRENT_TIMESTAMP
    WHERE NOMBRE = :NOMBRE_SECUENCIA;
    
    SUSPEND;
END;

-- Procedimiento: REINICIAR_SECUENCIA
