-- Procedimiento: CALCULAR_RETENCION_IVA
-- Calcula la retención de IVA (75% o 100% según tipo de contribuyente)
CREATE OR ALTER PROCEDURE CALCULAR_RETENCION_IVA (
    MONTO_IVA DECIMAL(18,2),
    CONTRIBUYENTE_ESPECIAL CHAR(1)
)
RETURNS (
    PORCENTAJE DECIMAL(5,2),
    MONTO_RETENIDO DECIMAL(18,2)
)
AS
BEGIN
    -- Si es contribuyente especial, retiene 100%
    -- Si es ordinario, retiene 75%
    IF (CONTRIBUYENTE_ESPECIAL = 'T') THEN
        PORCENTAJE = 100;
    ELSE
        PORCENTAJE = 75;
    
    MONTO_RETENIDO = (MONTO_IVA * PORCENTAJE) / 100;
    
    SUSPEND;
END;

-- Procedimiento: CALCULAR_IGTF
-- Calcula el IGTF (3% sobre pagos en divisas)
CREATE OR ALTER PROCEDURE CALCULAR_IGTF (
    TOTAL_OPERACION DECIMAL(18,2),
    ES_PAGO_DIVISAS CHAR(1)
)
RETURNS (
    TASA DECIMAL(5,2),
    MONTO_IGTF DECIMAL(18,2),
    TOTAL_CON_IGTF DECIMAL(18,2)
)
AS
BEGIN
    IF (ES_PAGO_DIVISAS = 'T') THEN
        TASA = 3;
        MONTO_IGTF = (TOTAL_OPERACION * TASA) / 100;
        TOTAL_CON_IGTF = TOTAL_OPERACION + MONTO_IGTF;
    ELSE
        TASA = 0;
        MONTO_IGTF = 0;
        TOTAL_CON_IGTF = TOTAL_OPERACION;
    
    SUSPEND;
END;

-- Procedimiento: CALCULAR_IVA
-- Calcula el IVA de una base imponible
CREATE OR ALTER PROCEDURE CALCULAR_IVA (
    BASE_IMPONIBLE DECIMAL(18,2),
    ALICUOTA DECIMAL(5,2)
)
RETURNS (
    MONTO_IVA DECIMAL(18,2),
    TOTAL_OPERACION DECIMAL(18,2)
)
AS
BEGIN
    MONTO_IVA = (BASE_IMPONIBLE * ALICUOTA) / 100;
    TOTAL_OPERACION = BASE_IMPONIBLE + MONTO_IVA;
    
    SUSPEND;
END;

-- Procedimiento: CALCULAR_RETENCION_ISLR
-- Calcula la retención de ISLR según el concepto
CREATE OR ALTER PROCEDURE CALCULAR_RETENCION_ISLR (
    BASE_IMPONIBLE DECIMAL(18,2),
    CONCEPTO VARCHAR(50)
)
RETURNS (
    PORCENTAJE DECIMAL(5,2),
    MONTO_RETENIDO DECIMAL(18,2)
)
AS
BEGIN
    -- Porcentajes comunes de retención ISLR en Venezuela
    IF (CONCEPTO CONTAINING 'HONORARIOS') THEN
        PORCENTAJE = 3;
    ELSE IF (CONCEPTO CONTAINING 'COMISION') THEN
        PORCENTAJE = 3;
    ELSE IF (CONCEPTO CONTAINING 'TRANSPORTE') THEN
        PORCENTAJE = 2;
    ELSE IF (CONCEPTO CONTAINING 'PUBLICIDAD') THEN
        PORCENTAJE = 2;
    ELSE IF (CONCEPTO CONTAINING 'SERVICIOS') THEN
        PORCENTAJE = 3;
    ELSE IF (CONCEPTO CONTAINING 'ALQUILER') THEN
        PORCENTAJE = 3;
    ELSE IF (CONCEPTO CONTAINING 'INTERES') THEN
        PORCENTAJE = 5;
    ELSE
        PORCENTAJE = 3; -- Porcentaje por defecto
    
    MONTO_RETENIDO = (BASE_IMPONIBLE * PORCENTAJE) / 100;
    
    SUSPEND;
END;

-- Procedimiento: CALCULAR_RETENCION_MUNICIPAL
-- Calcula la retención municipal según el municipio
CREATE OR ALTER PROCEDURE CALCULAR_RETENCION_MUNICIPAL (
    BASE_IMPONIBLE DECIMAL(18,2),
    MUNICIPIO VARCHAR(50)
)
RETURNS (
    PORCENTAJE DECIMAL(5,2),
    MONTO_RETENIDO DECIMAL(18,2)
)
AS
BEGIN
    -- Porcentajes comunes por municipio en Venezuela
    MUNICIPIO = UPPER(MUNICIPIO);
    
    IF (MUNICIPIO CONTAINING 'CARACAS') THEN
        PORCENTAJE = 2;
    ELSE IF (MUNICIPIO CONTAINING 'MARACAIBO') THEN
        PORCENTAJE = 2;
    ELSE IF (MUNICIPIO CONTAINING 'VALENCIA') THEN
        PORCENTAJE = 2;
    ELSE IF (MUNICIPIO CONTAINING 'BARQUISIMETO') THEN
        PORCENTAJE = 2;
    ELSE IF (MUNICIPIO CONTAINING 'GUAYANA') THEN
        PORCENTAJE = 1.5;
    ELSE
        PORCENTAJE = 1; -- Porcentaje general por defecto
    
    MONTO_RETENIDO = (BASE_IMPONIBLE * PORCENTAJE) / 100;
    
    SUSPEND;
END;

