-- Procedimiento: REPORTE_LIBRO_VENTAS
-- Genera el libro de ventas para un período fiscal
CREATE OR ALTER PROCEDURE REPORTE_LIBRO_VENTAS (
    PERIODO_INICIO VARCHAR(7),
    PERIODO_FIN VARCHAR(7)
)
RETURNS (
    FECHA_COMPROBANTE DATE,
    NUMERO_COMPROBANTE VARCHAR(20),
    RIF_CLIENTE VARCHAR(20),
    NOMBRE_CLIENTE VARCHAR(160),
    NUMERO_FACTURA VARCHAR(20),
    NUMERO_CONTROL VARCHAR(20),
    TOTAL_VENTA DECIMAL(18,2),
    BASE_IMPONIBLE DECIMAL(18,2),
    IMPUESTO_IVA DECIMAL(18,2),
    IMPUESTO_IVA_RETENIDO DECIMAL(18,2),
    VENTA_EXENTA DECIMAL(18,2),
    IGTF DECIMAL(18,2)
)
AS
BEGIN
    FOR SELECT
        V.FECHA_EMISION,
        V.DOCUMENTO,
        C.RIF,
        C.NOMBRE,
        V.DOCUMENTO,
        V.NUMERO_CONTROL,
        V.TOTAL_OPERACION,
        V.TOTAL_BASE_IMPONIBLE,
        V.TOTAL_IMPUESTO_FISCAL,
        V.TOTAL_RETENCION_IVA,
        V.TOTAL_EXENTO,
        COALESCE((SELECT SUM(IGTF) FROM PAGOS_VENTAS PV WHERE PV.VENTA_CORRELATIVO = V.CORRELATIVO), 0)
    FROM VENTAS V
    INNER JOIN CLIENTES C ON V.CLIENTE_CODIGO = C.CODIGO
    WHERE V.ESTATUS = 'R'
      AND V.IMPACTA_CONTABILIDAD = 'S'
      AND SUBSTRING(V.FECHA_EMISION FROM 1 FOR 7) BETWEEN :PERIODO_INICIO AND :PERIODO_FIN
    ORDER BY V.FECHA_EMISION, V.DOCUMENTO
    INTO
        :FECHA_COMPROBANTE,
        :NUMERO_COMPROBANTE,
        :RIF_CLIENTE,
        :NOMBRE_CLIENTE,
        :NUMERO_FACTURA,
        :NUMERO_CONTROL,
        :TOTAL_VENTA,
        :BASE_IMPONIBLE,
        :IMPUESTO_IVA,
        :IMPUESTO_IVA_RETENIDO,
        :VENTA_EXENTA,
        :IGTF
    DO
        SUSPEND;
END;

-- Procedimiento: REPORTE_LIBRO_COMPRAS
-- Genera el libro de compras para un período fiscal
CREATE OR ALTER PROCEDURE REPORTE_LIBRO_COMPRAS (
    PERIODO_INICIO VARCHAR(7),
    PERIODO_FIN VARCHAR(7)
)
RETURNS (
    FECHA_COMPROBANTE DATE,
    NUMERO_COMPROBANTE VARCHAR(20),
    RIF_PROVEEDOR VARCHAR(20),
    NOMBRE_PROVEEDOR VARCHAR(160),
    NUMERO_FACTURA VARCHAR(20),
    NUMERO_CONTROL VARCHAR(20),
    TOTAL_COMPRA DECIMAL(18,2),
    BASE_IMPONIBLE DECIMAL(18,2),
    IMPUESTO_IVA DECIMAL(18,2),
    IMPUESTO_IVA_RETENIDO DECIMAL(18,2),
    COMPRA_EXENTA DECIMAL(18,2),
    ISLR_RETENIDO DECIMAL(18,2)
)
AS
BEGIN
    FOR SELECT
        C.FECHA_DOCUMENTO,
        C.DOCUMENTO,
        P.RIF,
        P.NOMBRE,
        C.DOCUMENTO,
        C.NUMERO_CONTROL,
        C.TOTAL_OPERACION,
        C.TOTAL_BASE_IMPONIBLE,
        C.TOTAL_IMPUESTO_FISCAL,
        C.TOTAL_RETENCION_IVA,
        C.TOTAL_EXENTO,
        C.TOTAL_RETENCION_ISLR
    FROM COMPRAS C
    INNER JOIN PROVEEDORES P ON C.PROVEEDOR_CODIGO = P.CODIGO
    WHERE C.ESTATUS = 'R'
      AND C.IMPACTA_CONTABILIDAD = 'S'
      AND SUBSTRING(C.FECHA_DOCUMENTO FROM 1 FOR 7) BETWEEN :PERIODO_INICIO AND :PERIODO_FIN
    ORDER BY C.FECHA_DOCUMENTO, C.DOCUMENTO
    INTO
        :FECHA_COMPROBANTE,
        :NUMERO_COMPROBANTE,
        :RIF_PROVEEDOR,
        :NOMBRE_PROVEEDOR,
        :NUMERO_FACTURA,
        :NUMERO_CONTROL,
        :TOTAL_COMPRA,
        :BASE_IMPONIBLE,
        :IMPUESTO_IVA,
        :IMPUESTO_IVA_RETENIDO,
        :COMPRA_EXENTA,
        :ISLR_RETENIDO
    DO
        SUSPEND;
END;

